from neural_cryptanalysis.ciphers.base import BlockCipher


class Skinny128Cipher(BlockCipher):
    """
    SKINNY-128-128: 128-bit block, 128-bit key (TK1 only), 40 rounds.
    Reference: Beierle et al., CRYPTO 2016.
    """

    FULL_ROUNDS = 40

    # 8-bit S-box (operates on bytes)
    SBOX = [
        0x65, 0x4C, 0x6A, 0x42, 0x4B, 0x63, 0x43, 0x6B,
        0x55, 0x75, 0x5A, 0x7A, 0x53, 0x73, 0x5B, 0x7B,
        0x35, 0x8C, 0x3A, 0x81, 0x89, 0x33, 0x80, 0x3B,
        0x95, 0x25, 0x98, 0x2A, 0x90, 0x23, 0x99, 0x2B,
        0xE5, 0xCC, 0xE8, 0xC1, 0xC9, 0xE0, 0xC0, 0xE9,
        0xD5, 0xF5, 0xD8, 0xF8, 0xD0, 0xF0, 0xD9, 0xF9,
        0xA5, 0x1C, 0xA8, 0x12, 0x1B, 0xA0, 0x13, 0xA9,
        0x05, 0xB5, 0x0A, 0xB8, 0x03, 0xB0, 0x0B, 0xB9,
        0x32, 0x88, 0x3C, 0x85, 0x8D, 0x34, 0x84, 0x3D,
        0x91, 0x22, 0x9C, 0x2C, 0x94, 0x24, 0x9D, 0x2D,
        0x62, 0x4A, 0x6C, 0x45, 0x4D, 0x64, 0x44, 0x6D,
        0x52, 0x72, 0x5C, 0x7C, 0x54, 0x74, 0x5D, 0x7D,
        0xA1, 0x1A, 0xAC, 0x15, 0x1D, 0xA4, 0x14, 0xAD,
        0x02, 0xB1, 0x0C, 0xBC, 0x04, 0xB4, 0x0D, 0xBD,
        0xE1, 0xC8, 0xEC, 0xC5, 0xCD, 0xE4, 0xC4, 0xED,
        0xD1, 0xF1, 0xDC, 0xFC, 0xD4, 0xF4, 0xDD, 0xFD,
        0x36, 0x8E, 0x38, 0x82, 0x8B, 0x30, 0x83, 0x39,
        0x96, 0x26, 0x9A, 0x28, 0x93, 0x20, 0x9B, 0x29,
        0x66, 0x4E, 0x68, 0x41, 0x49, 0x60, 0x40, 0x69,
        0x56, 0x76, 0x58, 0x78, 0x50, 0x70, 0x59, 0x79,
        0xA6, 0x1E, 0xAA, 0x11, 0x19, 0xA3, 0x10, 0xAB,
        0x06, 0xB6, 0x08, 0xBA, 0x00, 0xB3, 0x09, 0xBB,
        0xE6, 0xCE, 0xEA, 0xC2, 0xCA, 0xE3, 0xC3, 0xEB,
        0xD6, 0xF6, 0xD8, 0xF8, 0xD3, 0xF3, 0xDB, 0xFB,
        0x31, 0x8A, 0x3E, 0x86, 0x8F, 0x37, 0x87, 0x3F,
        0x92, 0x21, 0x9E, 0x2E, 0x97, 0x27, 0x9F, 0x2F,
        0x61, 0x48, 0x6E, 0x46, 0x4F, 0x67, 0x47, 0x6F,
        0x51, 0x71, 0x5E, 0x7E, 0x57, 0x77, 0x5F, 0x7F,
        0xA2, 0x18, 0xAE, 0x16, 0x1F, 0xA7, 0x17, 0xAF,
        0x01, 0xB2, 0x0E, 0xBE, 0x07, 0xB7, 0x0F, 0xBF,
        0xE2, 0xCA, 0xEE, 0xC6, 0xCF, 0xE7, 0xC7, 0xEF,
        0xD2, 0xF2, 0xDE, 0xFE, 0xD7, 0xF7, 0xDF, 0xFF,
    ]

    # Tweakey permutation PT (16 bytes)
    PT = [9, 15, 8, 13, 10, 14, 12, 11, 0, 1, 2, 3, 4, 5, 6, 7]

    def __init__(self):
        super().__init__(block_bits=128, key_bits=128)
        self.RC = self._generate_rc()

    def _generate_rc(self):
        rc = []
        c = 0x01
        for _ in range(self.FULL_ROUNDS):
            rc.append(c)
            feedback = 1 ^ (((c >> 5) ^ (c >> 4)) & 1)
            c = ((c << 1) & 0x3F) ^ feedback
        return rc

    def _to_bytes(self, x: int) -> list:
        """128-bit int → list of 16 bytes, MSB first."""
        return [(x >> (120 - 8 * i)) & 0xFF for i in range(16)]

    def _from_bytes(self, bs: list) -> int:
        return sum(bs[i] << (120 - 8 * i) for i in range(16))

    def encrypt(self, plaintext: int, key: int, rounds: int) -> int:
        assert 1 <= rounds <= self.FULL_ROUNDS

        s  = self._to_bytes(plaintext)
        tk = self._to_bytes(key)

        for r in range(rounds):
            # SubBytes
            s = [self.SBOX[b] for b in s]

            # AddConstants
            c = self.RC[r]
            s[0]  ^= (c & 0xF)
            s[4]  ^= (c >> 4) & 0x3
            s[8]  ^= 0x02

            # AddRoundTweakey — XOR first two rows (8 bytes)
            for i in range(8):
                s[i] ^= tk[i]

            # ShiftRows
            s[4],  s[5],  s[6],  s[7]  = s[7],  s[4],  s[5],  s[6]
            s[8],  s[9],  s[10], s[11] = s[10], s[11], s[8],  s[9]
            s[12], s[13], s[14], s[15] = s[13], s[14], s[15], s[12]

            # MixColumns (same structure as SKINNY-64 but on bytes)
            for j in range(4):
                c0, c1, c2, c3 = s[j], s[4+j], s[8+j], s[12+j]
                s[j]    = c0 ^ c2 ^ c3
                s[4+j]  = c0
                s[8+j]  = c1 ^ c2
                s[12+j] = c0 ^ c2

            # Update Tweakey (permutation only — TK1, no LFSR)
            tk = [tk[self.PT[i]] for i in range(16)]

        return self._from_bytes(s)

    def random_key(self) -> int:
        import random
        return random.getrandbits(128)
